# Per-corpus BM25 cache: corpus_name -> (chunk_count, BM25_instance, source_chunk_counts)
_BM25_CACHE: Dict[str, Tuple[int, "BM25", Dict[str, int]]] = {}

# ---------------------------------------------------------------------------
# Chunk quality filter — penalise navigation/TOC/URL/summary garbage chunks
# so they don't crowd out actual data chunks after BM25 keyword boosting.
# ---------------------------------------------------------------------------
_URL_STRIP_RE = re.compile(r'https?://\S+')
_DOC_SUMMARY_RE = re.compile(r'^\[DOC SUMMARY')
_TIMESTAMP_RE = re.compile(r'\d{1,2}/\d{1,2}/\d{2,4},?\s*\d{1,2}:\d{2}\s*(?:AM|PM)?', re.I)
_TOC_NUMBERED_RE = re.compile(
    r'^\s*\d+[\.\)]\s+(?:Table|Chart|Figure|Appendix|Graph|Section)\b',
    re.MULTILINE | re.IGNORECASE,
)
# Detect section-outline lines: "  3.1  Some Title  ....  13"
_SECTION_OUTLINE_RE = re.compile(r'^\s*\d+(?:\.\d+)*\s+\w.*\s{4,}\d+\s*$', re.MULTILINE)


def _chunk_quality(chunk: "ChunkRecord") -> float:
    """Return a quality multiplier [0.1, 1.0] for *chunk*.

    Low scores are assigned to:
    • Doc-summary headers ([DOC SUMMARY: …])
    • URL-only or navigation fragments (mostly URL with little text)
    • Table-of-contents pages (numbered Table:/Figure: listings)
    • Section-outline pages (heading + big whitespace gap + page-number)
    • Micro-chunks (< 60 effective chars after stripping URLs/timestamps)
    """
    text = chunk.text
    n = len(text.strip())
    if n < 60:
        return 0.15

    # Doc summary headers (generated by the indexer — metadata, not content)
    if _DOC_SUMMARY_RE.match(text):
        return 0.20

    # Strip URLs and measure remaining content density
    text_no_url = _URL_STRIP_RE.sub('', text).strip()
    if len(text_no_url) < 50:
        return 0.10  # Almost entirely URL

    # Timestamp-only navigation chunks (web-scrape headers like "6/7/26, 10:05 PM ...")
    ts_count = len(_TIMESTAMP_RE.findall(text))
    if ts_count >= 1 and len(text_no_url.replace('\n', '').strip()) < 120:
        return 0.15

    lines = [l.strip() for l in text.splitlines() if l.strip()]

    # TOC: numbered "N. Table: …" or "N. Figure: …" listings
    toc_count = len(_TOC_NUMBERED_RE.findall(text))
    if toc_count >= 2 and toc_count / max(len(lines), 1) >= 0.25:
        return 0.20

    # Section outline: "3.1  Title          13" repeated lines
    outline_count = len(_SECTION_OUTLINE_RE.findall(text))
    if outline_count >= 3 and outline_count / max(len(lines), 1) >= 0.30:
        return 0.20

    return 1.0

# Max results from one source. Scales with corpus heterogeneity — in a corpus
# with 30+ source docs, capping at 3 is too tight for focused single-doc queries.
# Use _max_per_source(n_sources) to get the right cap dynamically.
_MAX_PER_SOURCE_BASE = 3
_MAX_PER_SOURCE_LARGE = 6   # for corpora with many diverse source documents

def _max_per_source(n_sources: int) -> int:
    """Return per-source cap depending on how many distinct sources are in play."""
    return _MAX_PER_SOURCE_LARGE if n_sources >= 15 else _MAX_PER_SOURCE_BASE

# Small-doc boost threshold: documents with fewer total chunks get a score boost
# to counteract the TF-IDF frequency disadvantage of short documents.
_SMALL_DOC_THRESHOLD = 500   # raised to 500 so short-but-dense docs also benefit
_SMALL_DOC_MAX_BOOST = 1.5


